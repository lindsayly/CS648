var value1 = prompt("Enter an integer");
var value2 = prompt("Enter another integer");
if (value1 > value2){
    document.write(value1 + " is the larger integer.");
}
if (value2 > value1){
    document.write(value2 + " is the larger integer.");
}
if (value1 == value2){
    document.write("The two values you entered are equal.");
}